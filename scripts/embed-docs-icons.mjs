import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const source = path.join(root, "assets", "docs-icons");
const output = path.join(root, "src", "routes", "docs-icons.generated.js");
const prefix = "/v1/docs/assets/";

const definitions = [
  ["favicon.ico", "image/x-icon"],
  ["favicon-16x16.png", "image/png"],
  ["favicon-32x32.png", "image/png"],
  ["apple-touch-icon.png", "image/png"],
  ["android-chrome-192x192.png", "image/png"],
  ["android-chrome-512x512.png", "image/png"],
  ["site.webmanifest", "application/manifest+json; charset=utf-8"],
];

const entries = definitions.map(([filename, contentType]) => {
  const file = path.join(source, filename);
  if (!fs.existsSync(file) || fs.statSync(file).size === 0) {
    throw new Error(`missing docs icon source: ${file}`);
  }
  if (filename === "site.webmanifest") JSON.parse(fs.readFileSync(file, "utf8"));
  return `  ${JSON.stringify(prefix + filename)}: Object.freeze({ contentType: ${JSON.stringify(contentType)}, base64: ${JSON.stringify(fs.readFileSync(file).toString("base64"))} })`;
});

fs.writeFileSync(
  output,
  [
    "// Generated by scripts/embed-docs-icons.mjs from assets/docs-icons.",
    "export const DOCS_ICONS = Object.freeze({",
    entries.join(",\n"),
    "});",
    "",
  ].join("\n"),
  "utf8",
);

console.log(`generated ${path.relative(root, output)} from ${definitions.length} local assets`);
