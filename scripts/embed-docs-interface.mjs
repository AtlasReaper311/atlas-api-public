import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const version = "0.1.1";
const source = path.join(root, "assets", "docs-interface", `v${version}`);
const manifestPath = path.join(source, "manifest.json");
const output = path.join(root, "src", "routes", "docs-interface.generated.js");
const stylesheet = "atlas-interface-kit.css";

function sha256(buffer) {
  return crypto.createHash("sha256").update(buffer).digest("hex");
}

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
if (manifest.version !== version || manifest.contract_version !== "2.0.0") {
  throw new Error("unexpected Atlas interface bundle version or contract");
}

for (const [filename, record] of Object.entries(manifest.files || {})) {
  const file = path.join(source, filename);
  const buffer = fs.readFileSync(file);
  if (buffer.byteLength !== record.bytes || sha256(buffer) !== record.sha256) {
    throw new Error(`Atlas interface bundle fingerprint mismatch: ${filename}`);
  }
  if (filename.endsWith(".json")) JSON.parse(buffer.toString("utf8"));
}

const css = fs.readFileSync(path.join(source, stylesheet));
fs.writeFileSync(
  output,
  [
    `// Generated by scripts/embed-docs-interface.mjs from Atlas Interface Kit v${version}.`,
    `export const DOCS_INTERFACE_VERSION = ${JSON.stringify(version)};`,
    "export const DOCS_INTERFACE_STYLESHEET = Object.freeze({",
    '  contentType: "text/css; charset=utf-8",',
    `  sha256: ${JSON.stringify(manifest.files[stylesheet].sha256)},`,
    `  base64: ${JSON.stringify(css.toString("base64"))},`,
    "});",
    "",
  ].join("\n"),
  "utf8",
);

console.log(`generated ${path.relative(root, output)} from Atlas Interface Kit v${version}`);
